/*
 * printk.h — 内核格式化控制台输出
 *
 * 用于内核诊断的最小 printf 风格函数。支持：
 *   %s — NUL 结尾字符串
 *   %d — 有符号十进制整数
 *   %u — 无符号十进制整数
 *   %x — 无符号十六进制（小写，64 位）
 *   %c — 单个字符
 *   %% — 字面量 %
 *
 * 输出到 uart_putc()（定义在 kernel/uart.h）。这是一个同步、
 * 忙等待实现 —— 无缓冲、无 IRQ 驱动输出。适用于 Phase 1–3 调试。
 */

#pragma once

/*
 * 向调试 UART 打印格式化字符串。
 *
 * 非中断安全（使用忙等待 UART 写入）。仅在 task 上下文或
 * 可接受阻塞的异常处理器中调用。
 */
void printk(const char *fmt, ...);
